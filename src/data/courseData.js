// Helper function to get node positions based on course level and semester
export const getNodePosition = (code) => {
  const course = courseData[code];
  if (!course) return { x: 0, y: 0 };

  // Base positions
  const BASE_X = 100;
  const BASE_Y = 100;
  const X_SPACING = 250;
  const Y_SPACING = 150;

  // Extract year and semester from course semester
  const [year, semester] = course.semester.split(' - ');
  const yearNum = parseInt(year);
  const isFirstSem = semester === '1st Semester';

  // Calculate position based on year and semester
  let x = BASE_X;
  let y = BASE_Y;

  // Year determines the row (y position)
  y = BASE_Y + Y_SPACING * (yearNum - 1);

  // Semester determines the column (x position)
  x = BASE_X + (isFirstSem ? 0 : X_SPACING);

  return { x, y };
};

// Helper function to generate edges based on prerequisites
export const generateEdges = (nodes) => {
  const edges = [];
  
  nodes.forEach(node => {
    const courseCode = node.id;
    const prerequisites = courseData[courseCode]?.prerequisites || [];
    
    prerequisites.forEach(prereq => {
      edges.push({
        id: `${prereq}-${courseCode}`,
        source: prereq,
        target: courseCode,
        type: 'smoothstep',
        animated: true,
        style: { stroke: '#8B0000', strokeWidth: 2 },
      });
    });
  });
  
  return edges;
}; 
